            add.setInt(1, id);
            add.setString(2, txt_name.getText());
            add.setString(3, cb_category.getSelectedItem().toString());
            add.setInt(4, Integer.valueOf(txt_qty.getText()));
            add.setInt(5, Integer.valueOf(txt_price.getText()));
            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Product Added Successfully");
            Con.close();
