    private void tbl_productMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_productMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tbl_product.getModel();
        int Myindex = tbl_product.getSelectedRow();
        
        String id = model.getValueAt(Myindex, 0).toString(); // Take the value from the id column in the selected row
        txt_id.setText(id); // Set the value into the JTextField txt_id
        txt_name.setText(model.getValueAt(Myindex, 1).toString());
        String gender = model.getValueAt(Myindex, 2).toString(); // Take the value from the gender column in the selected row
        cb_category.setSelectedItem(gender); // Set the selected item on the JComboBox cb_gender according to the value of gender
        String qty = model.getValueAt(Myindex, 3).toString(); // Take the value from the id column in the selected row
        txt_qty.setText(qty); // Set the value into the JTextField txt_id
        String price = model.getValueAt(Myindex, 4).toString(); // Take the value from the id column in the selected row
        txt_price.setText(price); // Set the value into the JTextField txt_id
    }//GEN-LAST:event_tbl_productMouseClicked

